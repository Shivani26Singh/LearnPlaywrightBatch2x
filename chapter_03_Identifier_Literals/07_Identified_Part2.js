// JavaScript Naming Conventions (Cases)

// 1. camelCase

let firstName = "Shivani";
let lastName = "Singh";

function functionName() {}


// 2. PascalCase

let FirstName = "Shivani";
let LastName = "Singh";

function FunctionName() {}

// 3. snake_case
// Words separated using underscore (_)

let first_name = "Shivani";
let total_price = 49.99;
let is_logged_in = false;


// 4. SCREAMING_SNAKE_CASE
// Used for constants and fixed values

const MAX_SIZE = 100;
const API_KEY = "abc123";
const DATABASE_URL = "localhost";


// 5. Hungarian Notation
// Older style where variable names include type prefixes

let strName = "Shivani"; // string
let bActive = true;     // boolean
let nCount = 5;         // number
let arrItems = [];      // array